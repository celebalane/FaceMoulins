				<p>Cette initiative poursuit un objectif: valoriser et faire accéder des publics sous-représentés dans la filière numérique (femmes, résidents des quartiers prioritaires, personnes sans diplômes ou en reconversion professionnelle), pour se former aux nouvelles technologies et trouver un emploi dans ce secteur en tension.</p>
				<h3>FACE et son Réseau, un moteur pour l'emploi et le numérique.</h3>
