				<h3>Une pédagogie innovante, des liens forts avec les entreprises.</h3>
				<p>A l'instar de son apprentissage du "code", les apprenants seront appelés à co-concevoir leur parcours d'insertion, pouvant notament déboucher sur des emplois de "développeur, développeuse web/Mobile" ou sur une création d'entreprise. Seront ainsi valorisés le travail collaboratif, l'apprentissage par l'action, l'accompagnement de pair à pair ...</p>
